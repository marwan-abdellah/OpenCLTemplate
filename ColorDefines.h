#define STD_BOLDRED     "\033[1m\033[31m"       /* Bold Red */
#define STD_BOLDGREEN   "\033[1m\033[32m"       /* Bold Green */
#define STD_BOLDYELLOW  "\033[1m\033[33m"       /* Bold Yellow */
